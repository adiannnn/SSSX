window.saveMatchingResultsData = function(results) {
    try {
        // 确保results是数组类型
        const resultsArray = Array.isArray(results) ? results : [];
        localStorage.setItem('matchingResultsData', JSON.stringify(resultsArray));
        window.matchingResultsData = resultsArray;
        console.log('匹配结果已保存到localStorage:', resultsArray.length, '条记录');
        return true;
    } catch (error) {
        console.error('保存匹配结果失败:', error);
        return false;
    }
};

