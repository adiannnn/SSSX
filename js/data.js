window.loadMatchingResultsData = function() {
    try {
        const data = localStorage.getItem('matchingResultsData');
        if (data) {
            window.matchingResultsData = JSON.parse(data);
            console.log('匹配结果数据已从localStorage加载:', window.matchingResultsData.length, '条记录');
        }
    } catch (error) {
        console.error('加载匹配结果数据出错:', error);
        window.matchingResultsData = [];
    }
    return window.matchingResultsData;
};

